#include "ft_putunbr.h"

void ft_putunbr(unsigned int n)
{
    if(n < 10)
    {
        ft_putchar(48 + n);
    }
    else
    {
        ft_putnbr(n / 10);
        ft_putnbr(n % 10);
    }
}
